// Playwright E2E test
test("submits form with valid data", async ({ page }) => {
  await page.fill('[placeholder="Paris..."]', "Paris");
  await page.fill('[placeholder="Paris in summer..."]', "Summer trip");
  await page.fill('[placeholder="Latitude"]', "48.8566");
  await page.fill('[placeholder="Longitude"]', "2.3522");
  await page.click('button[type="submit"]');
  await expect(page.textContent("Form submitted successfully")).toBeTruthy();
});

